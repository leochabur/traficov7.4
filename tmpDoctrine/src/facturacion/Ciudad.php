<?php



/**
 * Ciudad
 */
class Ciudad
{
    /**
     * @var string
     */
    private $ciudad;

    /**
     * @var integer
     */
    private $estructura;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set ciudad
     *
     * @param string $ciudad
     *
     * @return Ciudad
     */
    public function setCiudad($ciudad)
    {
        $this->ciudad = $ciudad;
    
        return $this;
    }

    /**
     * Get ciudad
     *
     * @return string
     */
    public function getCiudad()
    {
        return $this->ciudad;
    }

    /**
     * Set estructura
     *
     * @param integer $estructura
     *
     * @return Ciudad
     */
    public function setEstructura($estructura)
    {
        $this->estructura = $estructura;
    
        return $this;
    }

    /**
     * Get estructura
     *
     * @return integer
     */
    public function getEstructura()
    {
        return $this->estructura;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
}
