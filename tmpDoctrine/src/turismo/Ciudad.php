class Ciudad
{

	var $id;
	var $ciudad;
	var $estructura;

	function Ciudad()
	{
	}



	function getId()
	{
		return $this->id;
	}

	function getCiudad()
	{
		return $this->ciudad;
	}

	function getEstructura()
	{
		return $this->estructura;
	}		

	/**
	 * 
	 * @param newVal
	 */
	function setCiudad($newVal)
	{
		$this->ciudad = $newVal;
	}

	function setEstructura($newVal)
	{
		$this->estructura = $newVal;
	}	

	public function __toString()
	{
		return strtoupper($this->ciudad);
	}		

}
