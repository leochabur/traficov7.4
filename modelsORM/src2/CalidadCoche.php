<?php



use Doctrine\ORM\Mapping as ORM;

/**
 * CalidadCoche
 */
class CalidadCoche
{
    /**
     * @var string
     */
    private $calidad;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set calidad
     *
     * @param string $calidad
     * @return CalidadCoche
     */
    public function setCalidad($calidad)
    {
        $this->calidad = $calidad;

        return $this;
    }

    /**
     * Get calidad
     *
     * @return string 
     */
    public function getCalidad()
    {
        return $this->calidad;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var \Estructura
     */
    private $estructura;


    /**
     * Set id
     *
     * @param integer $id
     * @return CalidadCoche
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Set estructura
     *
     * @param \Estructura $estructura
     * @return CalidadCoche
     */
    public function setEstructura(\Estructura $estructura)
    {
        $this->estructura = $estructura;

        return $this;
    }

    /**
     * Get estructura
     *
     * @return \Estructura 
     */
    public function getEstructura()
    {
        return $this->estructura;
    }
}
