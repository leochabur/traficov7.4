class ConceptoPago
{

	var $id;
	var $concepto;




	function getId()
	{
		return $this->id;
	}

	function getConcepto()
	{
		return $this->concepto;
	}

	/**
	 * 
	 * @param newVal
	 */
	function setConcepto($newVal)
	{
		$this->concepto = $newVal;
	}

	public function __toString()
	{
		return strtoupper($this->concepto);
	}		

}
