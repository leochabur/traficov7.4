<?php



use Doctrine\ORM\Mapping as ORM;

/**
 * Darsena
 */
class Darsena
{
    /**
     * @var integer
     */
    private $posicion;

    /**
     * @var integer
     */
    private $id;

    /**
     * @var \LugarSalida
     */
    private $superior;

    /**
     * @var \LugarSalida
     */
    private $inferior;


    /**
     * Set posicion
     *
     * @param integer $posicion
     * @return Darsena
     */
    public function setPosicion($posicion)
    {
        $this->posicion = $posicion;

        return $this;
    }

    /**
     * Get posicion
     *
     * @return integer 
     */
    public function getPosicion()
    {
        return $this->posicion;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set superior
     *
     * @param \LugarSalida $superior
     * @return Darsena
     */
    public function setSuperior(\LugarSalida $superior = null)
    {
        $this->superior = $superior;

        return $this;
    }

    /**
     * Get superior
     *
     * @return \LugarSalida 
     */
    public function getSuperior()
    {
        return $this->superior;
    }

    /**
     * Set inferior
     *
     * @param \LugarSalida $inferior
     * @return Darsena
     */
    public function setInferior(\LugarSalida $inferior = null)
    {
        $this->inferior = $inferior;

        return $this;
    }

    /**
     * Get inferior
     *
     * @return \LugarSalida 
     */
    public function getInferior()
    {
        return $this->inferior;
    }
    /**
     * @var \Layout
     */
    private $layout;


    /**
     * Set layout
     *
     * @param \Layout $layout
     * @return Darsena
     */
    public function setLayout(\Layout $layout = null)
    {
        $this->layout = $layout;

        return $this;
    }

    /**
     * Get layout
     *
     * @return \Layout 
     */
    public function getLayout()
    {
        return $this->layout;
    }
}
