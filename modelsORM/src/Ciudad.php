<?php



/**
 * Ciudad
 */
class Ciudad
{
    /**
     * @var string
     */
    private $ciudad;

    /**
     * @var integer
     */
    private $estructura;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set ciudad
     *
     * @param string $ciudad
     *
     * @return Ciudad
     */
    public function setCiudad($ciudad)
    {
        $this->ciudad = $ciudad;
    
        return $this;
    }

    /**
     * Get ciudad
     *
     * @return string
     */
    public function getCiudad()
    {
        return $this->ciudad;
    }

    /**
     * Set estructura
     *
     * @param integer $estructura
     *
     * @return Ciudad
     */
    public function setEstructura($estructura)
    {
        $this->estructura = $estructura;
    
        return $this;
    }

    /**
     * Get estructura
     *
     * @return integer
     */
    public function getEstructura()
    {
        return $this->estructura;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    public function __toString()
    {
        return $this->ciudad;
    }
    /**
     * @var boolean
     */
    private $esCabecera;


    /**
     * Set esCabecera
     *
     * @param boolean $esCabecera
     * @return Ciudad
     */
    public function setEsCabecera($esCabecera)
    {
        $this->esCabecera = $esCabecera;

        return $this;
    }

    /**
     * Get esCabecera
     *
     * @return boolean 
     */
    public function getEsCabecera()
    {
        return $this->esCabecera;
    }
    /**
     * @var float
     */
    private $latitud;

    /**
     * @var float
     */
    private $longitud;

    /**
     * @var \Provincia
     */
    private $provincia;


    /**
     * Set latitud
     *
     * @param float $latitud
     * @return Ciudad
     */
    public function setLatitud($latitud)
    {
        $this->latitud = $latitud;

        return $this;
    }

    /**
     * Get latitud
     *
     * @return float 
     */
    public function getLatitud()
    {
        return $this->latitud;
    }

    /**
     * Set longitud
     *
     * @param float $longitud
     * @return Ciudad
     */
    public function setLongitud($longitud)
    {
        $this->longitud = $longitud;

        return $this;
    }

    /**
     * Get longitud
     *
     * @return float 
     */
    public function getLongitud()
    {
        return $this->longitud;
    }

    /**
     * Set id
     *
     * @param integer $id
     * @return Ciudad
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Set provincia
     *
     * @param \Provincia $provincia
     * @return Ciudad
     */
    public function setProvincia(\Provincia $provincia = null)
    {
        $this->provincia = $provincia;

        return $this;
    }

    /**
     * Get provincia
     *
     * @return \Provincia 
     */
    public function getProvincia()
    {
        return $this->provincia;
    }
}
