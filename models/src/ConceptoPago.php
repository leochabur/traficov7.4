<?php



use Doctrine\ORM\Mapping as ORM;

/**
 * ConceptoPago
 */
class ConceptoPago
{
    /**
     * @var string
     */
    private $concepto;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set concepto
     *
     * @param string $concepto
     * @return ConceptoPago
     */
    public function setConcepto($concepto)
    {
        $this->concepto = $concepto;

        return $this;
    }

    /**
     * Get concepto
     *
     * @return string 
     */
    public function getConcepto()
    {
        return $this->concepto;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
}
